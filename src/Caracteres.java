public class Caracteres {
    public static void main(String[] args) {
        // Tipos de caracteres en Java

        System.out.println("Tipos de caracteres en Java!");

        char cararter = 's'; // El tipo char solo acepta un solo caracter -- Son palabras reservadas de Java

        System.out.println("cararter = " + cararter);

        cararter = 65;
        System.out.println("cararter = " + cararter);

        String carrera = "Desarrallo de Software"; // String es un objeto de Java
        String usuario = "Pepe";
        System.out.println("Hola " + usuario +" tu carrera es "+carrera+"!");

    }


}
